package main

import "fmt"

func main()  {
	// harus di definisikan type data
	var year int = 202

	// bisa juga menyuruh go untuk mengira ngira type data
	age := 20

	// untuk constanta
	const name = "yudi"
	
	fmt.Println("Variable")
	fmt.Printf("year : %v\n", year)
	fmt.Println("name: ", name)
	fmt.Println("age: ", age)
}

